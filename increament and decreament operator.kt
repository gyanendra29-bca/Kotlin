fun main() {
    var a=10
    println(a)
    println("*******increament*****")
    println(++a) // this is prefix increament
    println(a++) // this is postfix increament
    println(a)
    println("********decreament******")
    println(--a) // this is prefix decreament
    println(a--) // this is postfix decreament
    println(a)

}